import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages, mode } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompts: Record<string, string> = {
      chat: `You are a senior AI consultant. You help users refine their project ideas, suggest tech stacks, identify risks, and provide actionable advice. Be concise, strategic, and professional. Use markdown formatting.`,
      plan: `You are a senior AI consultant generating a comprehensive strategic plan. Based on the user's project idea and any uploaded context, generate a structured plan with these exact sections using markdown:

# 📋 Project Overview
Summarize the project concept, target audience, and value proposition.

# 🏗️ Technical Architecture
Describe the recommended system architecture, including frontend, backend, database, and infrastructure.

# 🗺️ Development Roadmap
Break down the project into phases with estimated timelines (Phase 1: MVP, Phase 2: Growth, etc.)

# ⚠️ Risk Analysis
Identify key technical, market, and operational risks with mitigation strategies.

# 🛠️ Suggested Tech Stack
Recommend specific technologies with brief justifications.

# 💡 Recommendations
Provide additional strategic recommendations for success.

Be thorough, specific, and actionable. Use bullet points, tables, and clear formatting.`,
    };

    const systemContent = systemPrompts[mode] || systemPrompts.chat;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemContent },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const status = response.status;
      if (status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again shortly." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (status === 402) {
        return new Response(JSON.stringify({ error: "Usage limit reached. Please add credits." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("consulting-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
